package WrapperUndGenerics;

import javax.swing.*;

public class WasIstEinFramework {
    public static void main(String[] args) {

        /*
        Ein Framework ist eine Sammlung von Klassen und Interfaces, die eine bestimmte Funktionalität zur Verfügung stellen.
        Ein Framework ist eine Art Baukasten, der die Entwicklung von Anwendungen erleichtern soll.
        Man muss diese nur auswendig lernen und kann sie dann in seinen eigenen Programmen verwenden.
         */

        JFrame fenster1 = new JFrame("Mein erstes Fenster");
        //Breite = 400, Höhe = 300
        fenster1.setSize(400, 300);
        //Fenster wird sichtbar
        fenster1.setVisible(true);
    }
}
